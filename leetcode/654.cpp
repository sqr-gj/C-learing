#include <vector>
#include <algorithm>

using namespace std;

struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

//                                          2021/10/12
TreeNode* question_654(vector<int>& nums) {
    if (nums.empty())
        return nullptr;
    int max_num = *max_element(nums.begin(), nums.end());
    TreeNode *root = new TreeNode(max_num);
    int sz = nums.size();
    if (sz == 1)
        return root;

    int i;
    for (i = 0; i < sz; ++i) {
        if (nums[i] == max_num)
            break;
    }

    vector<int> left_num(nums.begin(), nums.begin() + i);
    vector<int> right_num(nums.begin() + i + 1, nums.end());

    root->left = question_654(left_num);
    root->right = question_654(right_num);

    return root;
}
