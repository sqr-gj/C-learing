struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

//递归                              2021/9/8
ListNode* question_24(ListNode* head) {
    if(head == nullptr || head->next == nullptr)
            return head;
    ListNode *Node = head->next;
    ListNode *Node_next = question_24(head->next->next);
    Node->next = head;
    head->next = Node_next;
    head = Node;
    return head;
}
